public class binatang {
    String nama;
    int umur;

    public binatang(String nama, int umur) {
        this.nama = nama;
        this.umur = umur;
    }

    public void makan() {
        System.out.println(nama + " Sedang makan.");
    }

    public void tidur() {
        System.out.println(nama + " Sedang tidur.");
    }

}


